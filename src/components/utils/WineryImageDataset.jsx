// Funzione per ottenere un'immagine random per una regione
export const getRegionalWineryImage = (region) => {
  const images = WINERY_IMAGES_DATASET[region] || WINERY_IMAGES_DATASET["Toscana"];
  const randomIndex = Math.floor(Math.random() * images.length);
  return images[randomIndex];
};

// Funzione per ottenere immagini basate su caratteristiche della cantina
export const getWineryImageByType = (region, tags = [], subRegion = "") => {
  const regionImages = WINERY_IMAGES_DATASET[region] || WINERY_IMAGES_DATASET["Toscana"];
  
  // Se è una cantina moderna, usa la prima immagine (solitamente più moderna)
  if (tags.includes("moderna") || tags.includes("innovativa")) {
    return regionImages[0];
  }
  
  // Se è storica/tradizionale, usa la seconda
  if (tags.includes("storica") || tags.includes("tradizionale")) {
    return regionImages[1];
  }
  
  // Se è biologica, usa la terza
  if (tags.includes("biologica") || tags.includes("naturale")) {
    return regionImages[2];
  }
  
  // Altrimenti immagine random della regione
  return getRegionalWineryImage(region);
};

